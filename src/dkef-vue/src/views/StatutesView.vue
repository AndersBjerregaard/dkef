          <h2 class="text-2xl font-bold text-theme-heading flex items-start gap-3">
            <span class="text-theme-accent font-serif text-3xl">§</span>
            <span>16. Opløsning</span>
          </h2>
          <div class="space-y-3 ml-12">
            <p class="text-base leading-relaxed text-theme-text">
              Opløsning af foreningen skal vedtages i overensstemmelse med de for vedtægtsændringer
              gældende regler, jvf. § 15. Dog kræves, at der på de respektive
              sektionsgeneralforsamlinger skal være mindst halvdelen af medlemmerne til stede, og at
              2/3 af de afgivne stemmer er for forslaget om opløsning.
            </p>
            <p class="text-base leading-relaxed text-theme-text">
              Såfremt forslaget bliver vedtaget med den fornødne majoritet, uden at halvdelen af
              medlemmerne er til stede, indkaldes til ny sektionsgeneralforsamling, på hvilken
              forslaget kan vedtages med den foreskrevne majoritet uanset antallet af fremmødte
              medlemmer.
            </p>
