            <p class="text-base leading-relaxed text-theme-text">
              Forslag fra hovedbestyrelsen til en vedtægtsændring forelægges
              sektionsgeneralforsamlingerne til eventuel vedtagelse jvf. § 5, og offentliggøres på
              hjemmesiden og i blad/nyhedsbrev.
            </p>
            <p class="text-base leading-relaxed text-theme-text">
              Sektionerne behandler herefter ændringsforslaget på en ordinær eller ekstraordinær
              generalforsamling. Til vedtagelse i de enkelte sektioner kræves, at 2/3 af de afgivne
              stemmer er for vedtagelse. Der skal foretages stemmeoptælling, og resultatet skal
              meddeles hovedbestyrelsen. Opnås den fornødne majoritet i samtlige sektioner, er
              forslaget vedtaget. Opnås majoriteten ikke i alle sektioner, foretages en
              sammentælling af samtlige ja- og nejstemmer, og forslaget er vedtaget, hvis der er
              mindst 4/5 af samtlige afgivne stemmer for vedtagelsen.
            </p>
          </div>
        </article>

